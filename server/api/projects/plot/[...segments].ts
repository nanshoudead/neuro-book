/** 规划层 Promise(读者债务账本)CRUD 与 beat 维护。物理删除只给 UI/人工,Agent 走 abandon。 */
async function handlePromises(event: H3Event, projectPath: string, method: string, segments: string[]): Promise<unknown> {
    if (method === "GET" && matchSegments(segments, ["promises"])) {
        return plotFacade.listStoryPromises(projectPath);
    }
    if (method === "POST" && matchSegments(segments, ["promises"])) {
        const body = await validateBody<CreateStoryPromiseRequestDto>(event, CreateStoryPromiseRequestDtoSchema);
        return plotFacade.createStoryPromise(projectPath, body);
    }
    if (segments.length === 2) {
        const promiseId = parseEntityId("promiseId", segments[1] ?? "");
        if (method === "GET") return plotFacade.getStoryPromiseDetailDto(projectPath, promiseId);
        if (method === "PATCH") {
            const body = await validateBody<UpdateStoryPromiseRequestDto>(event, UpdateStoryPromiseRequestDtoSchema);
            return plotFacade.updateStoryPromise(projectPath, promiseId, body);
        }
        if (method === "DELETE") return plotFacade.deleteStoryPromise(projectPath, promiseId);
    }
    // PUT /promises/:id/beats:upsert 一条 beat(同场同线仅一条);DELETE /promises/:id/beats/:sceneId:移除并跑回退检查。
    if (method === "PUT" && segments.length === 3 && segments[2] === "beats") {
        const promiseId = parseEntityId("promiseId", segments[1] ?? "");
        const body = await validateBody<SetPromiseBeatRequestDto>(event, SetPromiseBeatRequestDtoSchema);
        return plotFacade.setPromiseBeat(projectPath, promiseId, body);
    }
    if (method === "DELETE" && segments.length === 4 && segments[2] === "beats") {
        const promiseId = parseEntityId("promiseId", segments[1] ?? "");
        const sceneId = parseEntityId("sceneId", segments[3] ?? "");
        return plotFacade.removePromiseBeat(projectPath, promiseId, sceneId);
    }
    throw createError({statusCode: 404, message: "未知 Project Promise API"});
}

