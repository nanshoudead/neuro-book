/**
 * Code generator: converts Zod schemas to JSON Schema and injects
 * `defineRouteMeta({...})` calls into non-agent route files.
 *
 * Usage: bun scripts/build/generate-openapi-meta.ts
 *
 * How it works:
 * 1. Reads the route→schema mapping from `openapi-route-map.ts`
 * 2. Converts each Zod schema to JSON Schema via Zod v4's built-in `.toJSONSchema()`
 * 3. Constructs a `defineRouteMeta({...})` call with static JSON
 * 4. Injects (or replaces) the call in each route file
 *
 * The injected code is pure static JSON — no function calls, no variable refs.
 * This is REQUIRED because Nitro's build-time AST parser can only parse
 * ObjectExpression, ArrayExpression, and Literal AST nodes.
 */

import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { routeMetaMap, type RouteMetaEntry } from "../../server/openapi/route-map";

const rootDir = fileURLToPath(new URL("../../", import.meta.url));
const serverApiDir = resolve(rootDir, "server/api");

// ─── Marker comment for idempotent replacement ──────────────────

const MARKER = "// AUTO-GENERATED by generate-openapi-meta";

// ─── Zod → JSON Schema conversion ───────────────────────────────

/**
 * Convert a Zod schema to a JSON Schema object (POJO).
 * Uses Zod v4's built-in `.toJSONSchema()` with OpenAPI 3.0 target.
 *
 * Note: .toJSONSchema() handles:
 *   - ZodEffects (refine/superRefine/transform) → unwraps to inner type
 *   - ZodOptional → omits from required array
 *   - ZodNullable → adds "null" to type
 *   - ZodDefault → unwraps default value
 *   - ZodLazy → recursively resolves (with depth limit)
 */
function zodToJsonSchema(schema: any): Record<string, unknown> {
    try {
        // Use Zod v4's built-in toJSONSchema. The `unrepresentable: "ignore"` flag
        // skips transforms, effects, and other non-JSON-Schema-representable features
        // instead of throwing. We also use `io: "input"` to describe the request shape.
        const result = schema.toJSONSchema?.({
            target: "openapi-3.0",
            unrepresentable: "ignore",
        });
        if (result && typeof result === "object") {
            return result as Record<string, unknown>;
        }
    } catch (err) {
        console.warn(`  ⚠ toJSONSchema failed: ${(err as Error).message}, using empty schema`);
    }
    return {};
}

// ─── OpenAPI metadata generation ────────────────────────────────

function generateOpenAPIOperation(entry: RouteMetaEntry): Record<string, unknown> {
    const op: Record<string, unknown> = {
        tags: entry.tags,
        summary: entry.summary,
    };

    // Path parameters — Nitro auto-generates these, but we can document them
    // (The automatic ones from [param] in filenames are added by Nitro at build time)

    // Query parameters
    if (entry.queryParams) {
        const jsonSchema = zodToJsonSchema(entry.queryParams);
        const props = (jsonSchema.properties as Record<string, Record<string, unknown>>) ?? {};
        const required = (jsonSchema.required as string[]) ?? [];
        op.parameters = Object.entries(props).map(([name, schema]) => ({
            name,
            in: "query",
            required: required.includes(name),
            schema,
            ...(schema.description ? { description: schema.description } : {}),
        }));
    }

    // Request body
    if (entry.requestBody) {
        const schema = zodToJsonSchema(entry.requestBody);
        op.requestBody = {
            content: {
                "application/json": { schema },
            },
        };
    }

    // Responses
    if (entry.responseBody) {
        const schema = zodToJsonSchema(entry.responseBody);
        op.responses = {
            200: {
                description: "OK",
                content: {
                    "application/json": { schema },
                },
            },
        };
    } else {
        op.responses = {
            200: { description: "OK" },
            400: { description: "Bad Request — validation failed" },
        };
    }

    // Always add 400 for routes with request body validation
    if (entry.requestBody && !op.responses) {
        op.responses = {
            200: { description: "OK" },
            400: { description: "Bad Request — validation failed" },
        };
    } else if (entry.requestBody && op.responses && !(op.responses as Record<string, unknown>)["400"]) {
        (op.responses as Record<string, unknown>)["400"] = { description: "Bad Request — validation failed" };
    }

    return op;
}

// ─── Code string generation ─────────────────────────────────────

function generateDefineRouteMetaCall(entry: RouteMetaEntry): string {
    const operation = generateOpenAPIOperation(entry);
    const openAPIJson = JSON.stringify(operation, null, 4);

    return `${MARKER} — DO NOT EDIT
defineRouteMeta({
    openAPI: ${openAPIJson} as never,
});`;
}

// ─── File injection ─────────────────────────────────────────────

/**
 * Inject or replace `defineRouteMeta()` in a route file.
 * Handles three cases:
 * - File already has an AUTO-GENERATED defineRouteMeta → replace
 * - File has no defineRouteMeta → insert after last import
 * - File has no imports → insert at top (after file-level comments)
 */
function injectMeta(filePath: string, metaCall: string): boolean {
    const fullPath = resolve(serverApiDir, filePath);
    let content: string;
    try {
        content = readFileSync(fullPath, "utf-8");
    } catch {
        console.error(`  ✗ File not found: ${filePath}`);
        return false;
    }

    const hasMarker = content.includes(MARKER);

    if (hasMarker) {
        // Replace existing AUTO-GENERATED block
        // Match: MARKER line + defineRouteMeta(...)\n
        const regex = new RegExp(
            `${MARKER.replace(/[.*+?^${}()|[\]\\/]/g, "\\$&")}[^\n]*\\n` +
            `defineRouteMeta\\([\\s\\S]*?\\);\\n?`,
            "g"
        );
        content = content.replace(regex, metaCall + "\n\n");
        console.log(`  ↻ Replaced: ${filePath}`);
    } else {
        // Insert after last import statement
        const lastImportEnd = findLastImportEnd(content);

        if (lastImportEnd > 0) {
            // Skip past any trailing newlines after the last import
            let insertAt = lastImportEnd;
            while (insertAt < content.length && content[insertAt] === "\n") insertAt++;
            content = content.slice(0, lastImportEnd) + "\n\n" + metaCall + "\n\n" + content.slice(insertAt);
        } else {
            // No imports — insert at top after any file-level comment
            const commentEnd = findFileCommentEnd(content);
            const before = content.slice(0, commentEnd);
            const after = content.slice(commentEnd).replace(/^\n+/, "");
            content = before + (before ? "\n\n" : "") + metaCall + "\n\n" + after;
        }
        console.log(`  + Injected: ${filePath}`);
    }

    // Normalize: ensure single trailing newline
    content = content.replace(/\n{3,}/g, "\n\n").trimEnd() + "\n";

    writeFileSync(fullPath, content, "utf-8");
    return true;
}

/**
 * Find the end of the last import statement, correctly handling
 * multi-line imports like:
 *   import {
 *       foo,
 *       bar,
 *   } from "module";
 */
function findLastImportEnd(content: string): number {
    let lastEnd = 0;
    let i = 0;

    while (i < content.length) {
        // Look for "import" at start of line (possibly indented)
        const lineStart = i === 0 || content[i - 1] === "\n";
        if (!lineStart) {
            i++;
            continue;
        }

        // Check for "import " keyword
        if (content.startsWith("import ", i) || content.startsWith("import\t", i)) {
            // Find the "from" clause that terminates this import
            // Strategy: scan forward, tracking brace depth to skip over
            // multi-line destructured imports
            let j = i + 6; // skip "import"
            let braceDepth = 0;
            let fromIdx = -1;

            while (j < content.length) {
                const ch = content[j];

                if (ch === "{" && braceDepth >= 0) {
                    braceDepth++;
                } else if (ch === "}" && braceDepth > 0) {
                    braceDepth--;
                } else if (ch === "\n") {
                    // Check if this line starts with another import or export
                    const nextNonWs = content.slice(j + 1).match(/^\s*(\S)/)?.[1];
                    if (nextNonWs && (content.slice(j + 1).trimStart().startsWith("import ") ||
                                     content.slice(j + 1).trimStart().startsWith("export "))) {
                        break; // next statement is another import/export — this one ended without from
                    }
                } else if (braceDepth === 0) {
                    // Check for "from " at the top level
                    if (content.slice(j).match(/^from\s/)) {
                        fromIdx = j;
                        j += 4;
                        continue;
                    }
                }

                // If we found "from", scan to the end of this statement (semicolon or newline after string)
                if (fromIdx >= 0) {
                    // Skip past the string literal
                    if (ch === "\"" || ch === "'") {
                        const quote = ch;
                        j++;
                        while (j < content.length && content[j] !== quote) {
                            if (content[j] === "\\") j++; // skip escape
                            j++;
                        }
                        // j is now at the closing quote
                        j++; // skip closing quote
                        // Skip optional semicolon and whitespace
                        while (j < content.length && (content[j] === ";" || content[j] === " " || content[j] === "\t")) {
                            j++;
                        }
                        // Now at newline or next statement
                        lastEnd = j;
                        i = j;
                        break;
                    }
                }

                j++;
            }

            if (fromIdx < 0) {
                // Import without "from" (side-effect import like `import "foo"`)
                // Look for the string literal directly
                let j = i + 6;
                while (j < content.length && content[j] !== "\"" && content[j] !== "'" && content[j] !== ";") {
                    if (content[j] === "\n") break;
                    j++;
                }
                if (j < content.length && (content[j] === "\"" || content[j] === "'")) {
                    const quote = content[j];
                    j++;
                    while (j < content.length && content[j] !== quote) {
                        if (content[j] === "\\") j++;
                        j++;
                    }
                    j++; // skip closing quote
                    // Skip optional semicolon
                    while (j < content.length && (content[j] === ";" || content[j] === " " || content[j] === "\t")) j++;
                    lastEnd = j;
                }
                i = j;
                continue;
            }
            continue;
        }
        i++;
    }

    return lastEnd;
}

/**
 * Find the end of a file-level JSDoc comment block.
 */
function findFileCommentEnd(content: string): number {
    // Skip shebang
    let pos = 0;
    if (content.startsWith("#!")) {
        pos = content.indexOf("\n") + 1;
    }

    // Skip contiguous JSDoc /** ... */ or // comments at file top
    const commentRegex = /^(\s*\/\*\*[\s\S]*?\*\/\s*|\s*\/\/[^\n]*\s*)+/;
    const slice = content.slice(pos);
    const m = slice.match(commentRegex);
    if (m) {
        return pos + m[0].length;
    }
    return pos;
}

// ─── Main ───────────────────────────────────────────────────────

function main() {
    console.log(`Generating OpenAPI meta for ${routeMetaMap.length} routes...\n`);

    let injected = 0;
    let failed = 0;

    for (const entry of routeMetaMap) {
        const metaCall = generateDefineRouteMetaCall(entry);
        if (injectMeta(entry.file, metaCall)) {
            injected++;
        } else {
            failed++;
        }
    }

    console.log(`\nDone. ${injected} routes updated, ${failed} failed.`);
    console.log(`Run "bun dev" and visit /_swagger to verify.`);
}

main();
